<script lang="ts">
	import { get_backend_url } from "$lib/env"
	import type { MarketOrderCountByCreatedAt } from "$lib/types";
	import { Chart } from "chart.js/auto";
	import 'chartjs-adapter-date-fns';
	import { onMount } from "svelte";

	let data: MarketOrderCountByCreatedAt[];
	let canvas: HTMLCanvasElement;

	onMount(async() => {
		let response = await fetch(
			`${get_backend_url()}/statistics/orders?group_by=created_at`,
		);
		data = await response.json();
		
		new Chart(canvas, {
			type: "line",
			data: {
				labels: data.map(
					(d) => new Date(d.created_at),
				),
				datasets: [
					{
						data: data.map(
							(d) => d.count,
						),
						borderColor: "#7480ff",
						backgroundColor: "#7480ff"
					},
				],
			},
			options: {
				responsive: true,
				maintainAspectRatio: true,
				plugins: {
					legend: {
						display: false,
					},
				},
				animation: false,
				scales: {
					x: {
						type: "time",
						time: {
							unit: "day",
						},
						title: {
							display: true,
						},
                        grid: {
                            display: false
                        }
					},
					y: {
						title: {
							display: false,
						},
                        grid: {
                            display: false
                        }
					},
				},
			},
		});
	});
</script>

<canvas bind:this={canvas}></canvas>
